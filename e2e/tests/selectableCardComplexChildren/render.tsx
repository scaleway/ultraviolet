import { SelectableCard, SelectInput, Stack, Text } from '@ultraviolet/ui'
import { useState } from 'react'

const CheckboxSelectableCard = () => {
  const [value, setValue] = useState({ option1: false, option2: false })
  const [selectInputValue, setSelectInputValue] = useState('')

  return (
    <Stack flex={1} gap={1}>
      <SelectableCard
        checked={value.option1}
        label={
          <Stack direction="row" flex={1} justifyContent="space-between">
            <Text as="span" variant="bodyStrong">
              Optional option 1
            </Text>
            <Text as="span" variant="bodyStronger">
              4.99€
            </Text>
          </Stack>
        }
        name="option1"
        onChange={event =>
          setValue({ ...value, option1: event.currentTarget.checked })
        }
        showTick
        type="checkbox"
        value="option1"
      >
        <Stack gap={1}>
          This option will cost you 1.99€ and provide you with a lot of
          happiness checkbox
          <SelectInput
            label="Select a sub option checkbox"
            onChange={(newValue: string) => setSelectInputValue(newValue)}
            options={[
              {
                label: 'Sub option 1',
                value: 'option1',
              },
            ]}
            value={selectInputValue}
          />
        </Stack>
      </SelectableCard>
      <SelectableCard
        checked={value.option2}
        label={
          <Stack direction="row" flex={1} justifyContent="space-between">
            <Text as="span" variant="bodyStrong">
              Optional option 2
            </Text>
            <Text as="span" variant="bodyStronger">
              9.99€
            </Text>
          </Stack>
        }
        name="option2"
        onChange={event =>
          setValue({ ...value, option2: event.currentTarget.checked })
        }
        showTick
        type="checkbox"
        value="option2"
      >
        This option will cost you 2.99€ and provide you with a lot more of
        happiness checkbox
      </SelectableCard>
    </Stack>
  )
}

